// Helper: translate a content string (amenity, included item, ticket name/desc)
export const translateContent = (language: string, text: string): string => {
  if (language === 'en' || !text) return text;
  const langMap = contentTranslations[language];
  if (!langMap) return text;
  return langMap[text] || text; // Fall back to English if no translation
};
